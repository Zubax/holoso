"""Render a self-contained cocotb testbench string for a generated module.

The testbench embeds precomputed (input-bits, expected-float, tolerance) vectors and a minimal inline ZKF decoder, so
it runs standalone against the generated ``.v`` + ``holoso_support`` + Kulibin sources with only cocotb as a dependency.
"""

from __future__ import annotations

import json
from collections.abc import Callable

import numpy as np

from ..format import FloatFormat
from ..lir import Lir
from ..schedule import cycle_count
from .cosim import build_vectors, generic_sampler

_TEMPLATE = '''\
"""Auto-generated by Holoso: self-contained cocotb testbench for module @@MODULE@@."""

import json
import math

import cocotb
from cocotb.clock import Clock
from cocotb.triggers import FallingEdge, RisingEdge, Timer

_SPEC = json.loads(r"""@@SPEC@@""")


def _decode(wexp, wman, bits):
    wfrac = wman - 1
    width = wexp + wman
    exp_inf = (1 << wexp) - 1
    sign = (bits >> (width - 1)) & 1
    exp = (bits >> wfrac) & exp_inf
    frac = bits & ((1 << wfrac) - 1)
    if exp == 0:
        return 0.0
    if exp == exp_inf:
        return float("-inf") if sign else float("inf")
    value = math.ldexp((1 << wfrac) | frac, exp - ((1 << (wexp - 1)) - 1) - wfrac)
    return -value if sign else value


def _within(actual, expected, rtol, atol):
    if math.isinf(expected) or math.isinf(actual) or math.isnan(expected) or math.isnan(actual):
        return actual == expected
    return abs(actual - expected) <= atol + rtol * abs(expected)


@cocotb.test()
async def cosim(dut):
    wexp, wman = _SPEC["wexp"], _SPEC["wman"]
    timeout = int(_SPEC["timeout_cycles"])
    expected_cycles = int(_SPEC["cycles"])
    cocotb.start_soon(Clock(dut.clk, 10, unit="ns").start())
    await FallingEdge(dut.clk)
    dut.rst.value = 1
    dut.in_valid.value = 0
    for _ in range(4):
        await RisingEdge(dut.clk)
    dut.rst.value = 0
    await FallingEdge(dut.clk)
    dut.out_ready.value = 1

    for index, vec in enumerate(_SPEC["vectors"]):
        while int(dut.in_ready.value) != 1:
            await RisingEdge(dut.clk)
            await Timer(1, unit="ns")
        for name, bits in vec["in"].items():
            getattr(dut, "in_" + name).value = int(bits)
        dut.in_valid.value = 1
        await RisingEdge(dut.clk)
        await Timer(1, unit="ns")
        dut.in_valid.value = 0
        elapsed = 1
        while int(dut.out_valid.value) != 1:
            await RisingEdge(dut.clk)
            await Timer(1, unit="ns")
            elapsed += 1
            assert elapsed <= timeout, "timeout on vector %d" % index
        assert elapsed == expected_cycles, "vector %d cycle count: got %d expected %d" % (
            index,
            elapsed,
            expected_cycles,
        )
        for name, expected in vec["exp"].items():
            got = _decode(wexp, wman, int(getattr(dut, name).value))
            assert _within(got, expected, vec["rtol"], vec["atol"]), (
                "vector %d port %s: got %r expected %r" % (index, name, got, expected)
            )
        await RisingEdge(dut.clk)
        await Timer(1, unit="ns")
'''


def render_testbench(
    lir: Lir, fmt: FloatFormat, fn: Callable[..., object], *, count: int = 16, seed: int = 0xC0FFEE
) -> str:
    """Build embedded vectors with the generic sampler and splice them into the testbench template."""
    timeout = 4 * (sum(step.latency for step in lir.steps) + len(lir.inputs) + 8) + 64
    spec = build_vectors(
        fn,
        fmt,
        [load.name for load in lir.inputs],
        [wire.name for wire in lir.outputs],
        lir.op_count,
        count=count,
        rng=np.random.default_rng(seed),
        timeout_cycles=timeout,
        cycles=cycle_count(lir),
        sampler=generic_sampler,
    )
    return _TEMPLATE.replace("@@MODULE@@", lir.module_name).replace("@@SPEC@@", json.dumps(spec))
