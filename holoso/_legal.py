from functools import cache


@cache
def output_header(line_prefix: str = "", /) -> str:
    from holoso import __url__, __version__

    lines = [
        f"Generated by Holoso v{__version__} <{__url__}>.",
    ] + _LICENSE_WAIVER.splitlines()
    if line_prefix:
        lines = [f"{line_prefix}{line}" for line in lines]
    return "\n".join(lines)


_LICENSE_WAIVER = """
This file is generated output and is unencumbered: no copyright is claimed over it and no license terms apply.
You may use, modify, and redistribute it freely, in open-source or proprietary designs, without restrictions.
""".strip()
