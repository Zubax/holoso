#!/usr/bin/env python3
"""
Maintenance utility for vendoring external code.
"""

import shutil
import subprocess
import sys
import tempfile
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[1]
ZKF_PKG = REPO_ROOT / "holoso" / "_zkf"

REMOTE = "https://github.com/Zubax/zkf"
REF = "main"
# oracle.py pulls in mpmath, which Holoso does not depend on; __main__.py is upstream's CLI, unused here.
_MODEL_EXCLUDE = frozenset({"oracle.py", "__main__.py"})


def _git(args: list[str], cwd: Path) -> str:
    return subprocess.run(["git", *args], cwd=cwd, check=True, capture_output=True, text=True).stdout.strip()


def _replace_tree(src: Path, dst: Path, *, pattern: str, exclude: frozenset[str] = frozenset()) -> int:
    files = sorted(p for p in src.rglob(pattern) if p.name not in exclude)
    assert files, f"no {pattern!r} files under {src}"
    # dst is always a package subtree (rtl/, _model/), never the package root, so hand-authored siblings survive.
    if dst.exists():
        shutil.rmtree(dst)
    for path in files:
        target = dst / path.relative_to(src)
        target.parent.mkdir(parents=True, exist_ok=True)
        shutil.copyfile(path, target)
    return len(files)


def _readme(commit: str, commit_date: str) -> str:
    lines = [
        f"# {ZKF_PKG.name}",
        "",
        f"AUTO-GENERATED by `{Path(__file__).name}`. Do not edit by hand; refresh by re-running the script.",
        "",
        f"- Origin: <{REMOTE}>",
        f"- Ref: `{REF}`",
        f"- Commit: `{commit}` ({commit_date})",
    ]
    return "\n".join(lines)


def main() -> int:
    with tempfile.TemporaryDirectory(prefix="holoso_vendor_") as tmp:
        clone = Path(tmp) / "src"
        print(f"Cloning {REMOTE} @ {REF} ...")
        subprocess.run(["git", "clone", "--depth", "1", "--branch", REF, REMOTE, str(clone)], check=True)
        commit = _git(["rev-parse", "HEAD"], clone)
        commit_date = _git(["log", "-1", "--format=%cI"], clone)
        n_rtl = _replace_tree(clone / "hdl", ZKF_PKG / "rtl", pattern="*.v")
        n_model = _replace_tree(clone / "model" / "zkf", ZKF_PKG / "_model", pattern="*.py", exclude=_MODEL_EXCLUDE)
        (ZKF_PKG / "README.md").write_text(_readme(commit, commit_date), encoding="utf-8")
    print(f"Vendored {n_rtl} RTL + {n_model} model files into {ZKF_PKG.relative_to(REPO_ROOT)} (commit {commit[:12]})")
    return 0


if __name__ == "__main__":
    sys.exit(main())
